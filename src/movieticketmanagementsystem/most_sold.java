    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // TODO add your handling code here:

        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection connection = DriverManager.getConnection(
                "jdbc:sqlserver://localhost:1433;databaseName=movie_ticket_v1;selectMethod=cursor", "sa", "123456");

            Statement st = connection.createStatement();
            String queryG = "create view list_tab as SELECT m_id,m_nam FROM mv_va right JOIN ti_va ON mv_va.mv_id = ti_va.m_id;";
            st.addBatch(queryG);               
            
            st.executeBatch();
            String query1 = "select *,COUNT(m_id) as m_num from list_tab GROUP BY m_nam, m_id order by m_num DESC;";
            PreparedStatement pst =  connection.prepareStatement(query1);
 
            ResultSet rs = pst.executeQuery();
            DefaultTableModel dt =(DefaultTableModel)mTab.getModel();
            dt.setRowCount(0);
            while(rs.next()){
                Object o[] = {rs.getString("m_id"),rs.getString("m_nam"),rs.getString("m_num")};
                dt.addRow(o);
            }
            st.clearBatch();
            String clsV = "DROP VIEW list_tab;";
            st.addBatch(clsV);
            st.executeBatch();
        }

        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

